// Ported from vanilla keywords.js + slang.js

export type TermSection = 'keyword' | 'slang'

export interface TermCategory {
  label: string
  color: string
}

export interface Term {
  name: string
  cat: string
  section: TermSection
  desc: string
}

export const KEYWORD_CATS: Record<string, TermCategory> = {
  evergreen: { label: 'Evergreen',         color: '#5aa66a' },
  evasion:   { label: 'Evasion',           color: '#4f8cc9' },
  combat:    { label: 'Combat',            color: '#c94f4f' },
  cost:      { label: 'Cost / casting',    color: '#9867c5' },
  action:    { label: 'Keyword action',    color: '#2fb8a8' },
  counter:   { label: 'Counters',          color: '#d5ae5d' },
  zone:      { label: 'Graveyard / zones', color: '#d178b0' },
  game:      { label: 'Game / designation',color: '#d5a23a' },
}

export const SLANG_CATS: Record<string, TermCategory> = {
  archetype: { label: 'Archetype',       color: '#c94f4f' },
  slang:     { label: 'Slang',           color: '#4f8cc9' },
  play:      { label: 'In-game',         color: '#2fb8a8' },
  edh:       { label: 'Commander / EDH', color: '#d5a23a' },
  build:     { label: 'Deckbuilding',    color: '#5aa66a' },
  format:    { label: 'Format',          color: '#9867c5' },
}

export function getCat(section: TermSection, key: string): TermCategory {
  if (section === 'keyword') return KEYWORD_CATS[key] ?? KEYWORD_CATS.game
  return SLANG_CATS[key] ?? SLANG_CATS.slang
}

